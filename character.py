class Character:
    def __init__(self, character: str) -> None:
        self.character: str = character

        self.parts: list[str] = character.strip('\n').split('\n')
        self.height = len(self.parts)
        self.width = max(len(i) for i in self.parts)
        self.__adjust_character_width()

    def __adjust_character_width(self) -> None:
        self.parts = [i + ' ' * (self.width - len(i)) for i in self.parts]

    @staticmethod
    def get_character_object(character: str):
        characters_map = {
            'A': A, 'B': B, 'C': C, 'D': D, 'E': E, 'F': F,
            'G': G, 'H': H, 'I': I, 'J': J, 'K': K, 'L': L,
            'M': M, 'N': N, 'O': O, 'P': P, 'Q': Q, 'R': R,
            'S': S, 'T': T, 'U': U, 'V': V, 'W': W, 'X': X,
            'Y': Y, 'Z': Z
        }
        character = characters_map.get(character)
        if not character:
            raise f"character '{character}' is not supported"
        return Character(character)

    @staticmethod
    def get_space_object(space_seperation: int):
        return Character(' ' * space_seperation)

